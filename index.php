                <h3>Clasificación Científica</h3>
                <p>Existen diferentes variantes de cigarrillos electrónicos, y cada modelo específico tiene sus propias características únicas. El tamaño del cigarrillo electrónico influye en su capacidad, ya que un cuerpo más grande permite alojar un cartucho y una batería mayores. El cartucho se llena con e-líquido, mientras que la batería proporciona energía al vaporizador. Los cigarrillos electrónicos pueden agruparse en seis categorías, cada una de las cuales tiene sus propias características externas en cuanto a tamaño y forma. Algunos ejemplos serian los super mini, mini, mods y cigarros</p>
